package com.intercorp.api;

public class ResponseServicio {
	
	private Integer codEjecucion;
	private String	promedio;
	private String	desviacion;
	

	public Integer getCodEjecucion() {
		return codEjecucion;
	}
	public void setCodEjecucion(Integer codEjecucion) {
		this.codEjecucion = codEjecucion;
	}
	public String getPromedio() {
		return promedio;
	}
	public void setPromedio(String promedio) {
		this.promedio = promedio;
	}
	public String getDesviacion() {
		return desviacion;
	}
	public void setDesviacion(String desviacion) {
		this.desviacion = desviacion;
	}
	

}
